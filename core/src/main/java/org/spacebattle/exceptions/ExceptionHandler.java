package org.spacebattle.exceptions;

/**
 * Интерфейс обработчика исключений.
 * Позволяет централизованно логировать, подавлять или трансформировать ошибки,
 * возникающие при выполнении команд или других операций.
 */
public interface ExceptionHandler {

    /**
     * Обрабатывает исключение, возникшее в указанном источнике.
     *
     * @param source строковое имя класса или идентификатор команды/плагина
     * @param e      само исключение
     */
    void handle(String source, Exception e);
}
